#include <stdio.h>

int main()
{
    int int1,pozr=0,pozm=0,negr=0,negm=0;
    printf("Sayi giriniz:");
    scanf("%d",&int1);
    while(int1!=0){
      if(int1>0){
        pozm++;
        pozr+=int1;
      }
        if(int1<0){
        negm++;
        negr+=int1;
      }
      printf("Sayi giriniz:");
        scanf("%d",&int1);
    }
    printf("Pozitif sayi adeti: %d ",pozm);
    printf("Sayi toplami: %d \n",pozr);
    printf("Negatif sayi adeti: %d \t",negm);
    printf("Sayi toplami: %d",negr);
    return 0;
}
